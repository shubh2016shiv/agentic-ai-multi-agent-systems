#!/usr/bin/env python3
"""
============================================================
Trace Scoring and Evaluation
============================================================
Pattern 3: Attaching quality scores to Langfuse traces
using rule-based checks and LLM-as-judge evaluation.
Prerequisite: agent_metrics_and_cost.py

------------------------------------------------------------
OBJECTIVE
------------------------------------------------------------
Traces capture WHAT happened. Scores capture HOW WELL it
happened. In production multi-agent systems, you need both:

    Trace   -> "Agent recommended holding Spironolactone"
    Score   -> relevance=0.9, safety=1.0, completeness=0.7

Langfuse supports three types of scores:

    1. NUMERIC   -> float value (e.g., relevance: 0.85)
    2. CATEGORICAL -> label (e.g., quality: "good")
    3. BOOLEAN   -> pass/fail (e.g., safety_check: True)

Scores can be generated by:

    1. RULE-BASED  -> keyword checks, format validation
    2. LLM-AS-JUDGE -> second LLM evaluates the first's output
    3. HUMAN       -> manual annotation in Langfuse UI

This script demonstrates rule-based and LLM-as-judge scoring.

------------------------------------------------------------
OBSERVABILITY vs TRACEABILITY -- what this covers
------------------------------------------------------------
    TRACEABILITY:
        - What the agent produced (output content)
        - What the evaluator checked (scoring criteria)

    OBSERVABILITY:
        - Quality scores per agent output
        - Drift detection (are scores declining over time?)
        - Regression detection (did a model change drop quality?)
        - Filtering traces by score in Langfuse UI

------------------------------------------------------------
GRAPH TOPOLOGY
------------------------------------------------------------

    [START]
       |
       v
    [clinical_agent]      <-- generates a clinical response
       |
       v
    [rule_based_scorer]   <-- scores output using keyword/format rules
       |
       v
    [llm_judge_scorer]    <-- second LLM evaluates the first's output
       |
       v
    [END]

------------------------------------------------------------
SEQUENCE DIAGRAM
------------------------------------------------------------

    main()        clinical_agent      rule_based_scorer    llm_judge_scorer
      |                |                    |                    |
      |-- invoke() --> |                    |                    |
      |                |-- LLM(patient)     |                    |
      |                |-- response         |                    |
      |                |----- state ------->|                    |
      |                |                    |-- keyword checks   |
      |                |                    |-- format checks    |
      |                |                    |-- score: {relevance, safety, completeness}
      |                |                    |----- state ------->|
      |                |                    |                    |-- LLM(evaluate response)
      |                |                    |                    |-- parse scores
      |                |                    |                    |-- score: {accuracy, reasoning}
      |<-- result + all_scores -----------------------------------------|
      |                |                    |                    |

------------------------------------------------------------
HOW TO RUN
------------------------------------------------------------
    cd D:/Agentic AI/LangGraph_Multi_Agent_System
    python -m scripts.observability_and_traceability.trace_scoring_and_evaluation
============================================================
"""

# -- Standard library --------------------------------------------------------
import sys
import json
import re
from typing import TypedDict, Annotated

if hasattr(sys.stdout, "reconfigure"):
    sys.stdout.reconfigure(encoding="utf-8", errors="replace")


# -- LangGraph ---------------------------------------------------------------
from langgraph.graph import StateGraph, START, END
from langgraph.graph.message import add_messages

# -- LangChain ---------------------------------------------------------------
from langchain_core.messages import HumanMessage, SystemMessage

# -- Project imports ----------------------------------------------------------
from core.config import get_llm
from observability.callbacks import build_callback_config
from observability.tracer import get_langfuse_client


# ============================================================
# STAGE 3.1 -- State Definition
# ============================================================

class ScoringState(TypedDict):
    messages: Annotated[list, add_messages]
    patient_case: dict
    clinical_response: str
    rule_based_scores: dict      # from rule_based_scorer
    llm_judge_scores: dict       # from llm_judge_scorer
    all_scores_summary: str      # formatted summary for display


# ============================================================
# STAGE 3.2 -- Scoring Criteria (Rule-Based)
# ============================================================

# These are the clinical quality criteria used by the rule-based scorer.
# In production, these would be loaded from a config file or database.

REQUIRED_ELEMENTS = {
    "medication_mention": {
        "description": "Response mentions specific medications",
        "keywords": ["medication", "drug", "dose", "mg", "prescri", "spironolactone",
                      "lisinopril", "furosemide", "metformin"],
    },
    "monitoring_plan": {
        "description": "Response includes monitoring/follow-up instructions",
        "keywords": ["monitor", "follow-up", "recheck", "repeat", "schedule",
                      "review", "48 hours", "weekly"],
    },
    "contraindication_awareness": {
        "description": "Response addresses contraindications or risks",
        "keywords": ["contraindic", "risk", "caution", "avoid", "hold",
                      "discontinue", "reduce", "interaction"],
    },
    "patient_safety": {
        "description": "Response includes safety guidance",
        "keywords": ["consult", "healthcare provider", "emergency", "seek",
                      "immediately", "warning sign", "side effect"],
    },
}


# ============================================================
# STAGE 3.3 -- Node Definitions
# ============================================================

def clinical_agent_node(state: ScoringState) -> dict:
    """
    Clinical agent -- generates a response to be scored.

    This is the "primary" agent whose output will be evaluated
    by downstream scoring nodes.
    """
    llm = get_llm()
    patient = state["patient_case"]

    system = SystemMessage(content=(
        "You are a clinical decision support agent. Provide medication "
        "recommendations for this patient. Include: "
        "1) specific medication changes with doses, "
        "2) monitoring plan, "
        "3) contraindication awareness, "
        "4) patient safety guidance. "
        "End with 'Consult your healthcare provider.'"
    ))
    prompt = HumanMessage(content=f"""Patient Case:
Age: {patient.get('age')}y {patient.get('sex')}
Complaint: {patient.get('chief_complaint')}
Symptoms: {', '.join(patient.get('symptoms', []))}
Medical History: {', '.join(patient.get('medical_history', []))}
Medications: {', '.join(patient.get('current_medications', []))}
Labs: {json.dumps(patient.get('lab_results', {}))}

Provide your clinical recommendation.""")

    config = build_callback_config(
        trace_name="scoring_clinical_agent",
        tags=["scoring_demo"],
    )
    response = llm.invoke([system, prompt], config=config)

    print(f"    | [ClinicalAgent] Response: {len(response.content)} chars")

    return {
        "messages": [response],
        "clinical_response": response.content,
    }


def rule_based_scorer_node(state: ScoringState) -> dict:
    """
    Rule-based scoring -- evaluates the clinical response using
    keyword matching and format validation.

    Scores:
        medication_mention  (boolean) -> does it mention specific drugs?
        monitoring_plan     (boolean) -> does it include follow-up?
        contraindication    (boolean) -> does it address risks?
        patient_safety      (boolean) -> does it include safety guidance?
        word_count_adequate (boolean) -> >= 50 words?
        overall_completeness (numeric 0-1) -> fraction of checks passed
    """
    response_text = state.get("clinical_response", "")
    response_lower = response_text.lower()

    scores = {}
    checks_passed = 0
    total_checks = len(REQUIRED_ELEMENTS) + 1  # +1 for word count

    # -- Keyword checks ----------------------------------------------------
    for element_name, element_config in REQUIRED_ELEMENTS.items():
        keywords = element_config["keywords"]
        found = any(keyword in response_lower for keyword in keywords)
        scores[element_name] = found
        if found:
            checks_passed += 1
        print(f"    | [RuleScorer] {element_name}: {'PASS' if found else 'FAIL'}")

    # -- Format check: word count ------------------------------------------
    word_count = len(response_text.split())
    word_count_adequate = word_count >= 50
    scores["word_count_adequate"] = word_count_adequate
    scores["word_count"] = word_count
    if word_count_adequate:
        checks_passed += 1
    print(f"    | [RuleScorer] word_count: {word_count} ({'PASS' if word_count_adequate else 'FAIL'})")

    # -- Composite score ---------------------------------------------------
    overall_completeness = checks_passed / total_checks
    scores["overall_completeness"] = round(overall_completeness, 2)
    scores["checks_passed"] = checks_passed
    scores["total_checks"] = total_checks

    print(f"    | [RuleScorer] Completeness: {overall_completeness:.0%} ({checks_passed}/{total_checks})")

    # -- Attach scores to Langfuse trace (if available) --------------------
    langfuse_client = get_langfuse_client()
    if langfuse_client is not None:
        try:
            # In production, you would get the trace_id from the callback handler
            # and attach scores to that specific trace.
            # Here we demonstrate the API pattern:
            #   langfuse_client.score(
            #       trace_id=trace_id,
            #       name="completeness",
            #       value=overall_completeness,
            #   )
            print(f"    | [RuleScorer] Scores would be attached to Langfuse trace")
        except Exception as error:
            print(f"    | [RuleScorer] Could not attach scores: {error}")

    return {
        "rule_based_scores": scores,
    }


def llm_judge_scorer_node(state: ScoringState) -> dict:
    """
    LLM-as-Judge scoring -- uses a second LLM to evaluate the
    clinical response on quality dimensions.

    The judge LLM scores:
        clinical_accuracy   (1-5) -> are the facts correct?
        reasoning_quality   (1-5) -> is the logic sound?
        actionability       (1-5) -> can the doctor act on this?
        overall_rating      (1-5) -> composite quality

    This pattern is standard in LLM evaluation pipelines:
        Primary LLM produces output  ->  Judge LLM scores it
    """
    llm = get_llm()
    clinical_response = state.get("clinical_response", "")
    patient = state["patient_case"]

    judge_system = SystemMessage(content=(
        "You are an expert clinical evaluator. Score the following "
        "clinical recommendation on a scale of 1-5 for each criterion. "
        "Respond ONLY with a JSON object containing these keys: "
        "clinical_accuracy, reasoning_quality, actionability, overall_rating. "
        "Each value should be an integer from 1 to 5. "
        "Do not include any other text."
    ))

    judge_prompt = HumanMessage(content=f"""Evaluate this clinical recommendation:

PATIENT CONTEXT:
Age: {patient.get('age')}y {patient.get('sex')}
Medications: {', '.join(patient.get('current_medications', []))}
Labs: {json.dumps(patient.get('lab_results', {}))}

RECOMMENDATION TO EVALUATE:
{clinical_response}

Score as JSON (1-5 per criterion):
{{"clinical_accuracy": ?, "reasoning_quality": ?, "actionability": ?, "overall_rating": ?}}""")

    config = build_callback_config(
        trace_name="scoring_llm_judge",
        tags=["evaluation", "llm_as_judge"],
    )
    judge_response = llm.invoke([judge_system, judge_prompt], config=config)

    # Parse the judge's response as JSON
    judge_text = judge_response.content.strip()
    judge_scores = {}

    try:
        # Try to extract JSON from the response
        json_match = re.search(r'\{[^}]+\}', judge_text)
        if json_match:
            judge_scores = json.loads(json_match.group())
        else:
            judge_scores = json.loads(judge_text)
    except (json.JSONDecodeError, ValueError):
        # If JSON parsing fails, assign default scores
        print(f"    | [LLMJudge] Could not parse JSON, using defaults")
        judge_scores = {
            "clinical_accuracy": 3,
            "reasoning_quality": 3,
            "actionability": 3,
            "overall_rating": 3,
        }

    print(f"    | [LLMJudge] Scores: {json.dumps(judge_scores)}")

    # -- Build combined summary -------------------------------------------
    rule_scores = state.get("rule_based_scores", {})

    summary_lines = [
        "SCORING SUMMARY",
        "=" * 50,
        "",
        "Rule-Based Scores (automated):",
    ]
    for element_name in REQUIRED_ELEMENTS:
        passed = rule_scores.get(element_name, False)
        summary_lines.append(f"  {element_name}: {'PASS' if passed else 'FAIL'}")
    summary_lines.append(f"  word_count: {rule_scores.get('word_count', 0)}")
    summary_lines.append(f"  completeness: {rule_scores.get('overall_completeness', 0):.0%}")

    summary_lines.extend([
        "",
        "LLM-as-Judge Scores (1-5 scale):",
    ])
    for criterion, score in judge_scores.items():
        bar = "*" * int(score) + "." * (5 - int(score))
        summary_lines.append(f"  {criterion}: [{bar}] {score}/5")

    return {
        "messages": [judge_response],
        "llm_judge_scores": judge_scores,
        "all_scores_summary": "\n".join(summary_lines),
    }


# ============================================================
# STAGE 3.4 -- Graph Construction
# ============================================================

def build_scoring_graph():
    """Build the clinical agent + dual scorer pipeline."""
    workflow = StateGraph(ScoringState)

    workflow.add_node("clinical_agent", clinical_agent_node)
    workflow.add_node("rule_based_scorer", rule_based_scorer_node)
    workflow.add_node("llm_judge_scorer", llm_judge_scorer_node)

    workflow.add_edge(START, "clinical_agent")
    workflow.add_edge("clinical_agent", "rule_based_scorer")
    workflow.add_edge("rule_based_scorer", "llm_judge_scorer")
    workflow.add_edge("llm_judge_scorer", END)

    return workflow.compile()


